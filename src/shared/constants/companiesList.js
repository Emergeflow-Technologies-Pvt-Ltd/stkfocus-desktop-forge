export const filterCompaniesList = (searchString) => {
  // Get the first letter of the search string
  const searchKey = searchString?.toUpperCase()[0];

  // If the search key is not found in the companiesList, return an empty array
  if (!companiesList[searchKey]) {
    return [];
  }

  // Filter the companiesList based on the search string
  const filteredCompanies = companiesList[searchKey].filter((company) => {
    const companyName = company['Company Name']?.toUpperCase();
    const symbol = company.Symbol;
    return (
      companyName?.includes(searchString.toUpperCase()) ||
      symbol?.includes(searchString.toUpperCase())
    );
  });

  console.log('searchString', searchString);
  console.log('searchKey', searchKey);
  console.log(filteredCompanies);

  return filteredCompanies;
};
